				<h1><a href='post8.php'>Words to Avoid Using Near Your Boss</a></h1>
			</div>
			<div class='contents'>
				<p>I recently started an internship at Oracle. I'm working as a software engineer, with very little say in how anything is done, but I do get to suggest options and possibilities to my team lead and project managers.</p>
				<p>Luckily, this isn't my first rodeo. I've already been through the initially ugly "interfacing technical stuff with HR stuff" stage, and can now manage to give a suggestion to a higher-up without their eyes bugging out and my fearing for my job.</p>
				<p>It's really not a matter of what you say: you can pretty much be trying to get any point across, and have it come off as either good or bad. The more important thing is <i>how</i> you say it!</p>
				<p>The first step in the long process of not getting fired over your "helpful advice" is to avoid certain words and phrases. Just stop. Don't do it. When you're talking with your peers: fine, go ahead, but when you're not? Don't. Definitely not. Bad dog.</p>

				<dl>
					<dt>"Cost"</dt>
					<dd>Never, never, never ever use this word. Most likely, you've just suggested something that will <i>cost</i> - money, time, effort, whatever - now, at some perceived future gain. Which is all well and good: I'm a fan of future gains, and I'm sure most people are. But you can't go out throwing around words like "cost" willy-nilly! No, investors, managers, bankers, and everyone else with any sort of financial commitment don't want to hear the word cost - that evokes feelings of loss. As in "you paid the cost, now it's done" and "your money/time/effort is <i>gone</i>". Things being gone is a scary idea. Instead, you want to call your proposal an <b>investment</b>. People like investments, especially the people who make these sorts of decisions!</dd>

					<dt>"I think..."</dt>
					<dd>This one is incredibly common: "I think we should implement x because...", "I think it would be a good idea to...". Not only is this sentence structure typical of a schoolchild, but it suggests a lack of confidence which you do NOT want to show while proposing anything. You have to portray expertise and surety - otherwise why should they trust you?</dd>

					<dt>"Yes" or "No"</dt>
					<dd>OK, this one is a little crazy. Obviously there are certain times when yes or no are both perfectly valid responses, right? Well, not wrong, but those moments are few and far between. You should be in the habit not of saying yes or no, but saying "how?". As in: if your manager asks you to do... something... complicated or technically challenging or whatever, you don't want to answer right away. If you say no too often, you'll soon get fired for doing nothing. If you say yes too often, but can't/don't deliver, you'll get fired even sooner. But if you ask how, how to get the required support, funding, focus, et cetera, et cetera, and then follow through, you'll be much better off.</dd>

					<dt>Acronyms, especially technical ones.</dt>
					<dd>This one should be obvious: avoid the use of acronyms. "Yeah, the OS was FUBAR so, after CHROOTing from an SSD with BSD I...". No. No, you didn't. You "fixed the problem". Maybe you "utilized company backup policies to... fix the issue" or "fixed the <i>operating system</i> failure". Business people may or may not have any understand whatsoever of what our acronyms mean: we basically have a completely new language going, just with acronyms. Unless you're 100% absolutely completely sure they will understand you (which is virtually impossible), you should never use an acronym, especially technical ones. Yes, there are people who have no clue what a CPU is or what their OS is for.</dd>
				</dl>

				<p>There are many other important things to do (or avoid doing) in situations such as these, but avoiding these words TODAY will be instantly beneficial.</p>
