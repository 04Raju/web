				<p>In a few days, I'll be moving out of my on-campus residence (with a cafeteria) and be forced to - once again - begin making food for myself. I'm sure many of you are in the same situation, so I'll share some of my cooking tips and recipes to the less culinarily inclined among you.</p>

				<dl>
					<dt>You don't need to buy expensive ingredients</dt>
					<dd>Find a good local grocer, or buy common ingredients in bluk, to save money and find some great food as well. Pork shoulder? Cheap as anything, but one of my favorite meats.</dd>

					<dt>Get yourself a slow-cooker</dt>
					<dd>Now. Do it. You won't regret it. All of the parts of cooking you probably hate (spending time in front of a stove, getting home from work - exhausted - and needing to make yourself food on top of that, having to pay attention to the time) can be fixed by using a slow-cooker. And here, I'll share the super-secret best method of using one: throw in your food and spices in the morning and slow-cook it all day.</dd>

					<dt>Don't over-do the spices</dt>
					<dd>Besides the really obvious things like burning your salad or forgetting to cook your meat, over-seasoning your food is one of the worst things youc an possibly do. If you find yourself standing in front of you -as-yet-unseasoned food and thinking: "This spice is good... and so is this one, and this one, and this one..." YOU'RE DOING IT WRONG! Pick one seasoning that will go really well with your food, and maybe add a couple others which will go well with that first seasoning you picked. You can either use Google to figure out what goes well with what, or...</dd>

					<dt>Experiment!</dt>
					<dd>Cooking by recipes is all well and good, but to truly begin making great meals you'll have to put your head into it. Try new things, new combinations of flavours, different spices, etc. Maybe you'll find something amazing, and if not you'll at least gain some instincts as to what you shouldn't mix.</dd>

					<dt>Use basil</dt>
					<dd>Basil. Oh my God, basil. Basil is an amazing spice, incredibly delicious, and goes with most things. You have not tasted a real hamburger until you've ground up basil leaves into the beef. Get some basil, use your basil, love your basil (and use leaves instead of powder, if you can!).
				</dl>

				And now, for la piece de resistance, a few recipes for you <del>unsophisticated slobs</del> <ins>as-of-yet unculinarilized people</ins>.

				<dl>
					<dt>Beans Sofrito</dt>
					<dd>I learned this one from a penny-pinching friend of mine: it's gonna cost you less than $1 for a tasty and filling meal, if you buy some of the ingredients in bulk (ie instead of buying "a can of beans", buy "one metric shit-ton of beans"). And it's crazy easy to make. 
					<ol>
						<li>You'll need 1 can black beans, 1 cup rice, 1 small onion, 1 clove garlic, olive oil or butter, salt, pepper, Goya Sazon, Cayenne pepper.</li>
						<li>Boil 2 cups water, add rice and simmer (with a lid).</li>
						<li>In a hot sauce pan, add oilve oil or butter, sliced onion, smashed garlic. Fry until onion is glassy and add some salt and pepper.</li>
						<li>In the sauce pan, add Sazon and stir until you get a paste. Should take you about half a packet. Congratulations, you've just made yourself a sofrito.</li>
						<li>Pour all your beans and their juices into the saucepan, stir, add Cayenne pepper. Simmer it.</li>
						<li>When your rice is done, throw the beans on top.</li>
						<li>Eat. You've just made yourself at least two meals (delicious leftovers are delicious).</li>
					</ol>
					</dd>

					<dt>Pernil</dt>
					<dd>Got this one from the same friend as the Beans Sofrito recipe. Also incredibly delicious and incredibly cheap (again, buy bulk). One of my favorite recipes.
					<ol>
						<li>You'll need a pork shoulder, a few cloves garlic, Goya Sazon, salt, pepper, oil.</li>
						<li>Grind up garlic, Sazon, salt, pepper, and some oil. This is also a sofrito.</li>
						<li>Stab some wide holes in the pork shoulder, and stuff your sofrito into it. Rub the excess on the pork skin. Add salt and pepper.</li>
						<li>Bake it (300 degrees) skin-side up for a long time (45 minutes per pound).</li>
						<li>Now, personally I tend to avoid meat thermometers, but my friend swears by them, so... About an hour-and-a-half in, use your thermometer. When the meat is around 150-160 degrees, crank the oven up to 400 degrees.</li>
						<li>Take out the pork when it gets to 170 degrees. Important: wait at least ten minutes before you cut into the meat.</li>
						<li>Eat. This is also going to give you a huge amount of leftovers: a five-pound Pernil will feed you for a week. Savor its deliciousness.</li>
					</ol>
					</dd>

					<dt>Tostones</dt>
					<dd>Third and final recipe from the same penny-pincher. I think he got this one from his family. Enjoy it and remember: bacon is delicious.
					<ol>
						<li>Fry some bacon. We need the delicious, dribbling fat for this recipe, so feel free to just eat the bacon.</li>
						<li>Cut the skin off a plantain. Slice it up (about a centimetre / 1/2 inch per slice, maybe smaller) and throw the slices into a bowl of ice water.</li>
						<li>Pour enough oil into a sauce pan to submerged one of your slices of plantain. When it gets hot, throw in your bacon grease.</li>
						<li>Dry your plantain and fry them until they turn golden.</li>
						<li>Freeze them for ten minutes.</li>
						<li>Smash the plantain into tiny pieces on a cutting board with some flour. Fry them a second time, until they're golden and crispy.</li>
						<li>Eat. And the three recipes you just skimmed? They're all delicious together.</li>
					</ol>
					</dd>

